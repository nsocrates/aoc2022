import re

class Sand:
    INITIAL_POSITION = (500, 0)

    def __init__(self):
        self.x = self.INITIAL_POSITION[0]
        self.y = self.INITIAL_POSITION[1]

    def __iter__(self):
        yield self.x
        yield self.y

    def move(self, dx, dy):
        self.x += dx
        self.y += dy


class Solution:
    @staticmethod
    def parse_input(puzzle_input):
        points = set()
        max_depth = 0
        for line in puzzle_input.splitlines():
            lst = re.findall(r'(\d+)(?:,(\d+))', line)
            lst = [tuple(int(y) for y in x) for x in lst]
            px, py = lst[0]

            for i in range(1, len(lst)):
                cx, cy = lst[i]
                px, cx = sorted([px, cx])
                py, cy = sorted([py, cy])

                for j in range(px, cx + 1):
                    for k in range(py, cy + 1):
                        points.add((j, k))
                        max_depth = max(max_depth, k + 1)

                px, py = lst[i]
        return points, max_depth

    def __init__(self, puzzle_input):
         self.points, self.max_depth = self.parse_input(puzzle_input)

    def simulate(self):
        count = 0
        while Sand.INITIAL_POSITION not in self.points:
            sand = Sand()
            while True:
                if sand.y >= self.max_depth:
                    break
                if (sand.x, sand.y + 1) not in self.points:
                    sand.move(0, 1)
                elif (sand.x - 1, sand.y + 1) not in self.points:
                    sand.move(-1, 1)
                elif (sand.x + 1, sand.y + 1) not in self.points:
                    sand.move(1, 1)
                else:
                    break

            self.points.add(tuple(sand))
            count += 1
        return count

    def get_answer(self):
        return self.simulate()

